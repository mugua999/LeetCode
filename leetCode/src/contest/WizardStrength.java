	public static int totalStrength(int[] strength) {
		int totalStrength = 0, mod = (int) (Math.pow(10, 9) + 7);
		for (int i = 0; i < strength.length; i++) {
			for (int j = i + 1; j <= strength.length; j++) {
				int[] sub = Arrays.copyOfRange(strength, i, j);
				Arrays.sort(sub);
				for (int k = 0; k < sub.length; k++) {
					totalStrength = (totalStrength + ((sub[0] * sub[k]) % mod)) % mod;
				}
			}
		}

		return totalStrength;
	}
